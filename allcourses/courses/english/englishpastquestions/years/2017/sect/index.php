          <p>My desire to win gold and also see the sea drove me to a small coastal village. Though I had little food and drink, I covered a great distance. Towards dusk, I could sight the sea from a distance. I was really excited. Its immense size stretched as far as the eyes could see.<br />
          As the stars appeared in the sky, I ascended the hill and saw the village of my quest. I was dead with fatigue. To worsen my plight, there were blisters on my heel so I had to take a rest. But as I descended the hill, I was welcomed by the quietness of the place.<br />
          The villagers were enjoying the cool evening air. From the lagoon came the croaking of frogs. Children played and ran about excitedly. The village life was natural and simple. I sat on a bench close by. How good it was to rest!<br />
          I resolved to rest on the bench. As I lay there, a sheet of mist rolled from the sea and settled upon the village. In a few minutes, the village was filled with mist and everybody was running helter-skelter. I was drenched to the skin. I had decided to move to the next village when a woman suddenly walked up to me. For some time I had seen her gazing at me with pity. Now, as if she read my thoughts, she said, “If you would accept my hospitality, you will be sheltered till the morning. Just charity.” She was about thirty years of age, dressed in black with a pale face and dark eyes.
